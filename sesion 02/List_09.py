"""Listas en Python"""
"""Listas len() : Obtener el tamaño de una lista en pyhton"""

paises = []

print("El tamaño de mi variable es: {}".format(paises))

#usando append() parar agregar datos a mi lista
paises.append("Argentina")
paises.append("Perú")
paises.append("España")

print("Los valores de la lista es: {}".format(paises))
print("El tamaño actual de mi lista es: {}".format(len(paises)))
